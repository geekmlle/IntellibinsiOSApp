#import "UIColor+IntellibinsColor.h"

@implementation UIColor (IntellibinsColor)

+ (UIColor *)kIntellibinsGreen
{
    return [UIColor colorWithRed:(50/255.0) green:(129/255.0) blue:(127/255.0) alpha:1.0];
}

+ (UIColor *)kIntellibinsRed
{
    return [UIColor colorWithRed:(204/255.0) green:(0/255.0) blue:(0/255.0) alpha:1.0];
}

+ (UIColor *)kIntellibinsGray
{
    return [UIColor colorWithRed:(160/255.0) green:(160/255.0) blue:(160/255.0) alpha:1.0];
}

+ (UIColor *)kIntellibinsBlue
{
    return [UIColor colorWithRed:(51/255.0) green:(181/255.0) blue:(229/255.0) alpha:1.0];
}

+ (UIColor *)kIntellibinsYellow
{
    return [UIColor colorWithRed:(255/255.0) green:(187/255.0) blue:(51/255.0) alpha:1.0];
}

+ (UIColor *)kIntellibinsOrange
{
    return [UIColor colorWithRed:(266/255.0) green:(136/255.0) blue:(0/255.0) alpha:1.0];
}

+ (UIColor *)kIntellibinsLightRed
{
    return [UIColor colorWithRed:(255/255.0) green:(68/255.0) blue:(68/255.0) alpha:1.0];
}

+ (UIColor *)kIntellibinsPurple
{
    return [UIColor colorWithRed:(170/255.0) green:(102/255.0) blue:(204/255.0) alpha:1.0];
}

+ (UIColor *)kIntellibinsLightGreen
{
    return [UIColor colorWithRed:(153/255.0) green:(204/255.0) blue:(0/255.0) alpha:1.0];
}

@end
